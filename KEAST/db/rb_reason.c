int RbReasonInit(void)
{
	PRI_RBINF PriRbInf;
	TUINT CpuId;

	memset(&PriRbInf, 0, sizeof(PriRbInf));
	ScanRbReason(&PriRbInf);
#ifdef SST64
	if (PriRbInf.PCpuRbReg[8][PCPU_PC] == 0xAAAAAAAAAAAAAAAA && PriRbInf.PCpuRbReg[9][PCPU_PC] == 0xAAAAAAAAAAAAAAAA) /* MT6797 */
		PriRbInf.PCpuRbReg[8][PCPU_PC] = PriRbInf.PCpuRbReg[9][PCPU_PC] = 0;
	else
		ScanDfd(&PriRbInf);
#endif
	/* 设定register */
	for (CpuId = 0; CpuId < MAX_CORE; CpuId++) {
		RbUpdateRegs(&PriRbInf, CpuId);
	}
	return 1;
}


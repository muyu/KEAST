#include "task.h"
#include "kernel.h"


/*************************************************************************
*========================== init/deinit section ==========================
*************************************************************************/
/*************************************************************************
* DESCRIPTION
*	初始化kernel模块
*
* PARAMETERS
*	无
*
* RETURNS
*	0: kernel模块关闭
*	1: kernel模块可用
*
* GLOBAL AFFECTED
*
*************************************************************************/
int KernelInit(void)
{
	TaskInit();

	return 1;
}

/*************************************************************************
* DESCRIPTION
*	注销kernel模块
*
* PARAMETERS
*	无
*
* RETURNS
*	无
*
* GLOBAL AFFECTED
*
*************************************************************************/
void KernelDeInit(void)
{
	TaskDeInit();
}